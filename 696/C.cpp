void solve() {
	int n; cin >> n; n*=2;
	vector<int> a(n);
	multiset<int> s;
	for(int i=0 ; i<n ; i++) {
		cin >> a[i];
		s.insert(a[i]);
	}
	sort(a.begin(),a.end());
	for(int i=0 ; i<n-1 ; i++) {
		vector<pii> ans;
		ans.push_back({a[i],a[n-1]});
		multiset<int> t = s;
		t.erase(t.lower_bound(a[i]));
		t.erase(t.lower_bound(a[n-1]));
		int mx = a[n-1];
		bool fail = false;
		for(int j=0 ; j<n/2-1 ; j++) {
			auto it = t.end();
			it--;
			int cMxVal = *it;
			t.erase(it);
			int left = mx - cMxVal;
			if(!t.count(left)) {
				fail = true;
				break;
			} else {
				t.erase(t.lower_bound(left));
			}
			ans.push_back({cMxVal,left});
			mx = cMxVal;
		}
		if(!fail) {
			cout << "YES\n";
			cout << ans[0].ff + ans[0].ss << "\n";
			for(auto x : ans) {
				cout << x.ff << " " << x.ss << "\n";
			}
			return;
		}
	}
	cout << "NO\n";

}

