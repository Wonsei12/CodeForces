ll bin_pow(ll x, ll p) {
	if(p==0) return 1;
	if(p&1) return mult(x,bin_pow(x,p-1));
	return bin_pow(mult(x,x),p/2);
}
ll rev(ll x) {
	return bin_pow(x,MOD-2);
}

void solve() {
	ll x, n; cin >> x >> n;
	vector<ll> primes;
	for(int p=2 ; p*p<=x ; p++) {
		if(x%p) continue;
		primes.push_back(p);
		while(x%p==0)
			x/=p;
	}
	if(x!=1)
		primes.push_back(x);
	ll ans = 1;
	for(int i=0 ; i<sz(primes) ; i++) {
		ll tmp = n;
		ll cnt = 0;
		while(tmp > 0) {
			cnt += tmp / primes[i];
			tmp /= primes[i];
		}
		ans = mult(ans, bin_pow(primes[i],cnt));
	}
	cout << ans << "\n";
}

