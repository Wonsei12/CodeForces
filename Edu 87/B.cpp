using pii = pair<int, int>;
using ll = long long;
const ll MOD = 1e9 + 7;
const long double PI = acos(-1.0);

void solve() {
	string s; cin >> s;
	int n = sz(s);
	vector<vector<int>> cnt(n+1, vector<int>(3));
	for(int i=1 ; i<=n ; i++) {
		for(int j=0 ; j<3 ; j++) {
			cnt[i][j] = cnt[i-1][j];
			cnt[i][j] += (s[i-1] == '1' + j); 
		}
	}
	//debug(cnt);
	if(cnt[n][0]==0||cnt[n][1]==0||cnt[n][2]==0) {
		cout << 0 << "\n";
		return;
	}
	int l=1, r=n;
	int ans = -1;

	auto solve = [&](int len) -> bool {
		if(len > n)
			return false;
		for(int i=1 ; i+len<=n ; i++) {
			bool is1 = cnt[i+len][0]-cnt[i-1][0] >= 1;
			bool is2 = cnt[i+len][1]-cnt[i-1][1] >= 1;
			bool is3 = cnt[i+len][2]-cnt[i-1][2] >= 1;
			if(is1&&is2&&is3)
				return true;
		}
		return false;
	};

	while(l<=r) {
		int mid = (l+r) / 2;
		if(solve(mid)) {
			ans = mid;
			r = mid - 1;
		} else {
			l = mid + 1;
		}
	}
	cout << ans + 1 << "\n";
}

